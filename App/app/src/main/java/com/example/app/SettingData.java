package com.example.app;

public class SettingData {
    public int tempMax, tempMin, temp, time;

    public SettingData() {
    }

    public SettingData(int tempMax, int tempMin, int temp, int time) {
        this.tempMax = tempMax;
        this.tempMin = tempMin;
        this.temp = temp;
        this.time = time;
    }
}
